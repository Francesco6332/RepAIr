import * as Print from 'expo-print';
import * as Sharing from 'expo-sharing';

export type PdfDiagnosisData = {
  vehicle: string;
  date: string;
  probableIssue: string;
  confidence: number;
  urgency: string;
  costMin: number;
  costMax: number;
  safetyAdvice: string;
  nextChecks?: string[];
};

const URGENCY_COLOR: Record<string, string> = {
  low: '#34D399',
  medium: '#FBBF24',
  high: '#FB923C',
  critical: '#F87171',
};

function buildHtml(data: PdfDiagnosisData): string {
  const urgencyColor = URGENCY_COLOR[data.urgency.toLowerCase()] ?? '#FBBF24';
  const confidencePct = Math.round(data.confidence * 100);
  const checksHtml = data.nextChecks?.length
    ? `<ul class="checks">${data.nextChecks.map((c) => `<li>${c}</li>`).join('')}</ul>`
    : '';

  return `<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8"/>
<meta name="viewport" content="width=device-width, initial-scale=1.0"/>
<style>
  * { box-sizing: border-box; margin: 0; padding: 0; }
  body {
    font-family: -apple-system, Helvetica Neue, Arial, sans-serif;
    background: #0d0d14;
    color: #e8e8f0;
    padding: 40px 36px;
    min-height: 100vh;
  }
  .header {
    display: flex;
    align-items: center;
    justify-content: space-between;
    margin-bottom: 32px;
    padding-bottom: 20px;
    border-bottom: 1px solid rgba(255,255,255,0.08);
  }
  .logo { font-size: 22px; font-weight: 800; letter-spacing: -0.5px; color: #a78bfa; }
  .logo span { color: #e8e8f0; }
  .meta { font-size: 12px; color: rgba(255,255,255,0.4); text-align: right; line-height: 1.6; }
  .section {
    background: rgba(255,255,255,0.04);
    border: 1px solid rgba(255,255,255,0.08);
    border-radius: 16px;
    padding: 20px 22px;
    margin-bottom: 16px;
  }
  .section-title {
    font-size: 10px;
    font-weight: 700;
    text-transform: uppercase;
    letter-spacing: 1px;
    color: rgba(255,255,255,0.35);
    margin-bottom: 10px;
  }
  .issue { font-size: 20px; font-weight: 700; line-height: 1.35; color: #e8e8f0; }
  .row { display: flex; align-items: center; gap: 14px; flex-wrap: wrap; margin-top: 14px; }
  .badge {
    display: inline-flex;
    align-items: center;
    padding: 5px 12px;
    border-radius: 8px;
    font-size: 12px;
    font-weight: 700;
  }
  .bar-wrap { margin-top: 12px; }
  .bar-label { display: flex; justify-content: space-between; font-size: 11px; color: rgba(255,255,255,0.45); margin-bottom: 6px; }
  .bar-bg { height: 6px; background: rgba(255,255,255,0.08); border-radius: 3px; overflow: hidden; }
  .bar-fill { height: 6px; border-radius: 3px; background: #a78bfa; }
  .advice {
    font-size: 13px;
    line-height: 1.65;
    color: rgba(255,255,255,0.65);
  }
  .checks { padding-left: 18px; margin-top: 4px; }
  .checks li { font-size: 13px; line-height: 1.8; color: rgba(255,255,255,0.65); }
  .vehicle-name { font-size: 16px; font-weight: 700; color: #e8e8f0; }
  .footer {
    margin-top: 32px;
    padding-top: 16px;
    border-top: 1px solid rgba(255,255,255,0.06);
    font-size: 11px;
    color: rgba(255,255,255,0.25);
    text-align: center;
    line-height: 1.6;
  }
</style>
</head>
<body>
  <div class="header">
    <div class="logo">Rep<span>AI</span>ro</div>
    <div class="meta">
      ${data.vehicle}<br/>
      ${data.date}
    </div>
  </div>

  <div class="section">
    <div class="section-title">Diagnosis result</div>
    <div class="issue">${data.probableIssue}</div>
    <div class="row">
      <span class="badge" style="background:${urgencyColor}22;color:${urgencyColor};border:1px solid ${urgencyColor}55;">
        ${data.urgency} urgency
      </span>
      <span class="badge" style="background:rgba(167,139,250,0.12);color:#a78bfa;border:1px solid rgba(167,139,250,0.3);">
        €${data.costMin}–€${data.costMax} est.
      </span>
    </div>
    <div class="bar-wrap">
      <div class="bar-label">
        <span>Confidence</span>
        <span>${confidencePct}%</span>
      </div>
      <div class="bar-bg">
        <div class="bar-fill" style="width:${confidencePct}%"></div>
      </div>
    </div>
  </div>

  <div class="section">
    <div class="section-title">Safety advice</div>
    <div class="advice">${data.safetyAdvice}</div>
  </div>

  ${data.nextChecks?.length ? `
  <div class="section">
    <div class="section-title">Recommended checks</div>
    ${checksHtml}
  </div>` : ''}

  <div class="footer">
    This report is an AI-generated pre-diagnosis and does not replace a professional mechanic inspection.<br/>
    Generated by RepAIro · repairoapp.com
  </div>
</body>
</html>`;
}

export async function shareDiagnosisPdf(data: PdfDiagnosisData): Promise<void> {
  const html = buildHtml(data);
  const { uri } = await Print.printToFileAsync({ html, base64: false });
  const canShare = await Sharing.isAvailableAsync();
  if (canShare) {
    await Sharing.shareAsync(uri, {
      mimeType: 'application/pdf',
      dialogTitle: 'Share diagnosis report',
      UTI: 'com.adobe.pdf',
    });
  } else {
    await Print.printAsync({ uri });
  }
}
